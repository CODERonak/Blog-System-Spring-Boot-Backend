package com.project.BlogSystem.auth.security;

import java.util.ArrayList;
import java.util.Collection;
import java.util.List;

import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;

import com.project.BlogSystem.auth.model.entity.AuthUser;

import lombok.AllArgsConstructor;

/*

User details implementation for Spring Security.
with methods to get user authorities, password, and username.


*/

@AllArgsConstructor
public class UserDetailsImpl implements UserDetails {

    private final AuthUser user;

    @Override
    public Collection<? extends GrantedAuthority> getAuthorities() {
        List<GrantedAuthority> authorities = new ArrayList<>();
        authorities.add(new SimpleGrantedAuthority("ROLE_" + user.getRole().toString()));
        return authorities;
    }

    @Override
    public String getPassword() {
        return user.getPassword();
    }

    @Override
    public String getUsername() {
        return user.getUsername();
    }
}
